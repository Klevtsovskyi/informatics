#Чи складаються 2 рядки з одних і тих же символів

def samechars (s1, s2):
    '''Чи складаються 2 рядки s1, s2 з одних і тих же символів

    '''
    m1 = set(s1)
    m2 = set(s2)
    return m1 == m2

s1 = input("введіть 1 рядок: ")
s2 = input("введіть 2 рядок: ")

print('Рядки', end=' ')
if not samechars(s1,s2):
    print('не', end=' ')
print('складаються з одних і тих же символів')



      


