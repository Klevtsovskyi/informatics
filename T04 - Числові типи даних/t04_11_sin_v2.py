#Обчислення sin(x) з уникненням переповнення


x = float(input('введіть x: '))
eps = float(input('введіть eps: '))


#для не дуже великих х працювати не буде. Можна спробувати х==50.

z = x
y = x
k = 0
while abs(z) >= eps:
    k = k+1
    z = -z*x*x/(2*k*(2*k+1))
    y += z
    print('k=',k,'z=',z,'y=',y) #спробуємо з'ясувати причину 


print ('Результат ',y)
