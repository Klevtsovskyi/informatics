#Обчислення заданого числа Фібоначчі (РС 2 порядку) з використанням кортежів

n = int(input('введіть n: '))
x1 = 1      #x1 - нульове число Фібоначчі
x2 = 1      #x2 - перше число Фібоначчі

for k in range(n):
    x1, x2 = x2, x1+x2      #при присвоєнні кортежу третя змінна стає непотрібною

print ('Результат', x1)
